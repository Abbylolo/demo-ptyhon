# 练习：
# 1. 打印1-100之间的所有偶数
n = 2
while n <= 100:
    print(f'第1题：{n}')
    n += 2

# 2.求 1-100之间可以被6整除的数的个数
n = 1
cnt = 0
while n <= 100:
    if n % 6 == 0:
        cnt += 1
    n += 1
print(f'第2题：{cnt}')

# 3. 打印1-100之间的所有奇数
n = 1
while n <= 100:
    print(f'第3题：{n}')
    n += 2

# 4.计算1到100以内所有偶数的和。
n = 2
s = 0
while n <= 100:
    s += n
    n += 2
print(f'第4题：{s}')

# 5.计算1到100以内所有能被3或者7整除的数的和。
n = 1
s = 0
while n <= 100:
    if n % 3 == 0 or n % 7 == 0:
        s += n
    n += 1
print(f'第5题：{s}')

# 6.计算1到100以内能同时被7和3整除的数的个数。
n = 1
cnt = 0
while n <= 100:
    if n % 3 == 0 and n % 7 == 0:
        cnt += 1
    n += 1
print(f'第6题：{cnt}')

