# 打印一个等腰三角形, 要求一次只能打印一个"*"
'''          i   空格数量     *的数量
    *        1     4(=5-i)     1(=2*i-1)
   ***       2     3           3
  *****      3     2           5 
 *******     4     1           7
*********    5     0           9
'''
i = 1
while i <= 5:
    j = 1
    while j <= 9:
        if j <= 5 - i:
            print(' ', end='')
        elif j >= 5 + i:
            print(' ', end='')
        else:
            print('*', end='')
        j += 1
    i += 1
    print('')



